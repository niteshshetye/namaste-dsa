// LOOPS COVERD
for (let i = 0; i < 5; i++) {
  console.log("Hello world ", i);
}
console.log("****************");

// REVERSE LOOP
for (let i = 5; i > 0; i--) {
  console.log("Hello world ", i);
}
console.log("****************");

// LOOP THROUGH ARRAY
const arr = [1, 2, 3, 4, 5];
for (let i = 0; i < arr.length; i++) {
  console.log("Array element ", arr[i]);
}
console.log("****************");

// WHILE LOOP
let j = 0;
while (j < 5) {
  console.log("Hello world ", j);
  j++;
}
console.log("****************");
